import type { VisualKind } from "@/components/CategoryVisuals";

/**
 * Pricing plan that sits inside a service. Shape stays identical across
 * the 7 services so the <PricingCard /> template can render any plan.
 */
export interface PricingPlan {
  tier: "BASIC" | "STANDARD" | "PREMIUM";
  emoji: string;
  subtitle: string;
  features: string[];
  tools: string[];
  delivery: string;
  support: string;
  /** Bonus items "Free X" — rendered as 🎁 chips above the price. */
  gifts?: string[];
  marketPrice: string;
  offerPrice: string;
  /** Highlights the "most popular" / hero tier. Always PREMIUM here. */
  recommended?: boolean;
}

export interface Service {
  slug: string;
  /** Hooks into the existing themed CategoryVisual mock-up. */
  kind: VisualKind;
  /** CSS --accent used for borders, glows, fills on the page. */
  accent: string;
  num: string;
  /** Display badge above title: "WEBSITE DEVELOPMENT". */
  category: string;
  /** Big page H1, also the same word that appears on the home card. */
  title: string;
  tagline: string;
  promise: string;
  /** Short status badges shown under the hero. */
  highlights: string[];
  perfectForLabel: string;
  perfectFor: string[];
  examplesLabel: string;
  examples: string[];
  plans: [PricingPlan, PricingPlan, PricingPlan];
}

